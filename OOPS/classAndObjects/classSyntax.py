class Calc:
    def add(self, a, b):
        print(a + b)

    def sub(self, a, b):
        print(a - b)

    def mult(self, a, b):
        print(a * b)

    def div(self, a, b):
        print(a / b)

    def rem(self, a, b):
        print(a % b)


Calc().add(1, 2)
Calc().sub(5, 2)
Calc().mult(2, 3)
Calc().div(10, 5)
Calc().rem(5, 2)
